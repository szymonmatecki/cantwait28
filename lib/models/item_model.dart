class ItemModel {
  ItemModel({
    required this.id,
    required this.title,
    required this.imageURL,
    required this.relaseData,
  });

  final String id;
  final String title;
  final String imageURL;
  final DateTime relaseData;

  String daysLeft() {
    return relaseData.difference(DateTime.now()).inDays.toString();
  }
}
